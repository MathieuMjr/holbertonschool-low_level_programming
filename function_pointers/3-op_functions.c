#include "3-calc.h"

/**
 * op_add - function that do additions with 2 ints
 * @a: first operator
 * @b: 2nd operator
 *
 * Return: resultat of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that do subtractions with 2 ints
 * @a: first operator
 * @b: 2nd operator
 *
 * Return: resultat of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function that do multiplications beetween 2 ints
 * @a: first operator
 * @b: 2nd operator
 *
 * Return: resultat of a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function that do divisions with 2 ints
 * @a: first operator
 * @b: 2nd operator
 *
 * Return: resultat of a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that gives the rest of a division of 2 ints
 * (modulo)
 * @a: first operator
 * @b: 2nd operator
 *
 * Return: resultat of a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
